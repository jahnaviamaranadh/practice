function least(a,b)
{
    if(a<b)
    {
        return("a is the least number");
    }
    else
    {
        return("b is the least number");
    }
}
console.log(least(10,20));
console.log(least(-1,0));
console.log(least(50,40));
console.log(least(100,10));
console.log(least(1,20));
console.log(least(-4,-1));